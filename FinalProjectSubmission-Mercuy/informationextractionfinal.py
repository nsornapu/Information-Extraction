def dependecy_parsing(input):
  dependency_parser = StanfordDependencyParser(path_to_jar, path_to_model_jar)
  result = dependency_parser.raw_parse(input)
  dep = next(result)
  return list(dep.triples())

def most_common(lst):
  return max(set(lst), key=lst.count)

