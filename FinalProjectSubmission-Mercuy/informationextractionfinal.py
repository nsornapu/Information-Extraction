  for chunk in nltk.ne_chunk(input):
    if hasattr(chunk, 'label'):
      if (chunk.label() == 'ORGANIZATION'):
      #print(chunk.label(), ' '.join(c[0] for c in chunk))
        output.append([chunk.label(),"".join(c[0] for c in chunk)])
  return output

